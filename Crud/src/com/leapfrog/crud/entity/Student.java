package com.leapfrog.crud.entity;

/**
 *
 * @author chira
 */
public class Student {
    private String name,gender,email;
    private int id;
    
    public Student() {
    }

    public Student(String name, String gender, String email, int id) {
        this.name = name;
        this.gender = gender;
        this.email = email;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
}
