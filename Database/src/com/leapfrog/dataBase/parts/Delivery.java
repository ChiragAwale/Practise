package com.leapfrog.dataBase.parts;

/**
 *
 * @author chira
 */
public class Delivery {
    private int noOfVehicles, noOfOutlets, price;

    public int getNoOfVehicles() {
        return noOfVehicles;
    }

    public void setNoOfVehicles(int noOfVehicles) {
        this.noOfVehicles = noOfVehicles;
    }

    public int getNoOfOutlets() {
        return noOfOutlets;
    }

    public void setNoOfOutlets(int noOfOutlets) {
        this.noOfOutlets = noOfOutlets;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
}
